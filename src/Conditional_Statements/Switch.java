package Conditional_Statements;

public class Switch {
	public static void main(String[] args) {
		
	
	int choice = 2;
	switch (choice) {
	case 1: System.out.println(" You chose one");
	break;
	case 2 : System.out.println("You chose two");
	break;
	default : System.out.println(" Enter 1 or 2 ");
	break;
	
	}
	
	
	}
}
